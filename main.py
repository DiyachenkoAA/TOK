import tkinter as tk
from tkinter import filedialog, messagebox

# Создание главного окна
root = tk.Tk()
root.title('Распознавание объектов методом минимальных расстояний')
root.geometry("1366x768")

# ============================================================
# 1. НАЗВАНИЕ ПРОГРАММЫ
# ============================================================
title_label = tk.Label(
    root, 
    text='РАСПОЗНАВАНИЕ ОБЪЕКТОВ',
    font=('Arial', 18, 'bold')
)
title_label.place(x=500, y=50)

# ============================================================
# 2. ФАМИЛИЯ РАЗРАБОТЧИКА
# ============================================================
developer_label = tk.Label(
    root,
    text='Разработчик: Дьяченко А.А.',
    font=('Arial', 12)
)
developer_label.place(x=500, y=100)

# ============================================================
# 3. НАЗВАНИЕ ВУЗА
# ============================================================
university_label = tk.Label(
    root,
    text='СибГМУ\nКафедра медицинской кибернетики',
    font=('Arial', 12),
    justify=tk.CENTER
)
university_label.place(x=500, y=150)

# ============================================================
# 4. ИНФОРМАЦИОННАЯ МЕТКА
# ============================================================
info_label = tk.Label(
    root, 
    text='Программа разработана на кафедре медицинской кибернетики',
    font=('Arial', 12)
)
info_label.place(x=500, y=250)

# ============================================================
# 5. КНОПКА ОТКРЫТИЯ ФАЙЛА
# ============================================================
# 
def on_button_click():
    file_path = filedialog.askopenfilename()
    if file_path:
        messagebox.showinfo("Файл выбран", f"Выбран файл: {file_path}")

button = tk.Button(
    root, 
    text='Открыть файл данных', 
    command=on_button_click,
    font=('Arial', 12),
    bg='lightblue',
    padx=20,
    pady=10
)
button.place(x=400, y=350)  # Сдвинута влево

# ============================================================
# 6. КНОПКА ВЫХОДА
# ============================================================
exit_button = tk.Button(
    root,
    text='Выход',
    command=root.quit,
    font=('Arial', 12),
    bg='lightcoral',
    padx=30,
    pady=10
)
exit_button.place(x=800, y=350)  # Сдвинута вправо (дальше от первой кнопки)

root.mainloop()