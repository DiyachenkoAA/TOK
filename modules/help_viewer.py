import tkinter as tk
from tkinter import Toplevel, Text, Scrollbar

def show_help(parent):
    """Показать справку"""
    window = Toplevel(parent)
    window.title("Справка - HELP")
    window.geometry("800x600")
    
    text_area = Text(window, wrap=tk.WORD, font=('Arial', 11))
    scrollbar = Scrollbar(window, command=text_area.yview)
    text_area.configure(yscrollcommand=scrollbar.set)
    scrollbar.pack(side=tk.RIGHT, fill=tk.Y)
    text_area.pack(side=tk.LEFT, fill=tk.BOTH, expand=True)
    
    help_text = """
    ╔══════════════════════════════════════════════════════════════════════════════╗
    ║                    ПРОГРАММА РАСПОЗНАВАНИЯ ОБЪЕКТОВ                         ║
    ║                      Метод минимальных расстояний                           ║
    ╚══════════════════════════════════════════════════════════════════════════════╝
    
    1. НАЗНАЧЕНИЕ ПРОГРАММЫ
    -----------------------
    Программа предназначена для классификации объектов методом минимальных расстояний
    (ближайшего центроида). Позволяет загружать обучающую выборку, обучать классификатор,
    распознавать новые объекты и оценивать качество классификации.
    
    2. ФОРМАТ ВХОДНЫХ ДАННЫХ
    ------------------------
    • Поддерживаются файлы Excel (.xlsx, .xls) и CSV (.csv)
    • Первый столбец: метки классов (текст)
    • Остальные столбцы: числовые признаки
    • Минимальное количество признаков: 2
    • Максимальное количество признаков: не ограничено
    
    3. МЕТОДЫ ОБУЧЕНИЯ
    ------------------
    • Метод минимальных расстояний (центроиды):
      - Вычисляются центроиды (средние значения) для каждого класса
      - Новый объект относится к ближайшему центроиду
    
    • Метод минимального числа ошибок (персептрон):
      - Строится линейная дискриминантная функция f(x) = w·x + b
      - Веса подбираются для минимизации числа ошибок классификации
    
    4. МЕТРИКИ РАССТОЯНИЯ
    ---------------------
    • Евклидово расстояние: d = √[(x₁-y₁)² + (x₂-y₂)² + ...]
    • Манхэттенское расстояние: d = |x₁-y₁| + |x₂-y₂| + ...
    
    5. ПОРЯДОК РАБОТЫ
    -----------------
    1. Загрузите обучающую выборку (Файл → Считать данные)
    2. Выберите признаки для обучения (Обучение → Выбор признаков)
    3. Обучите классификатор (Обучение → Обучение модели)
    4. Оцените качество распознавания (Обучение → Оценка качества)
    5. Распознавайте новые объекты (Распознавание → Распознать объект)
    
    6. ГОРЯЧИЕ КЛАВИШИ
    ------------------
    • Ctrl+O - Загрузить данные
    • Ctrl+S - Сохранить данные
    • Ctrl+T - Обучение модели
    • Ctrl+F - Выбор признаков
    • Ctrl+E - Оценка качества
    • Ctrl+R - Распознать объект
    • Ctrl+Q - Выход из программы
    • F1 - Справка
    
    7. ОЦЕНКА КАЧЕСТВА
    ------------------
    • Матрица ошибок (confusion matrix)
    • Общая точность распознавания (%)
    • Точность распознавания по каждому классу
    • Количество ошибок классификации
    
    8. ВОЗМОЖНЫЕ ОШИБКИ
    -------------------
    • Недостаточно признаков - выберите минимум 2 признака
    • Некорректные данные - проверьте формат входного файла
    • Ошибка при обучении - проверьте наличие числовых данных
    
    © 2026 СибГМУ, Кафедра медицинской кибернетики
    Разработчик: Дьяченко А.А.
    Версия: 2.0
    """
    
    text_area.insert(tk.END, help_text)
    text_area.configure(state='disabled')